adultContainer
.html($("<div/>").html(container = $("<ul/>")));
var adultCategories = new Array();
$.each(adult.data,function(i,item){
	if(item.category.indexOf(" ")){ // we have more than one category
		tempArray = item.category.split(" ");
		$.each(tempArray,function(i2,item2){
			adultCategories.push(item2)
		})
	}else{
		adultCategories.push(item.category)	
	}
	
	container
		.append(
			$("<li/>",{css:{"display":"none"}})
				.addClass("researchList "+item.category)
				.html(
					function(){
						if(item.libraryOnly && !inside){// its a library only item and the user is not inside
							return $("<div/>")
								.html($("<span/>",{"class":"iconic book"}))
								.append("<span/>",{html:item.title})
						}
						return $("<div/>")
						.html(
							function(){
								if(item.libraryOnly){
									return $("<span/>",{"class":"iconic book",css:{"margin-right":"3px","cursor":"help"},"title":"Available at the library only"})
								}else{
									return $("<span/>",{"class":"iconic",css:{"margin-right":"3px"}})
								}
							})
							.append(
								$("<a/>")
								.attr({"title":item.title,"href":function(){
									if(inside){
										return item.link_in;
									}else{
										return item.link_out;
									}
								}})
								.html(item.title)
							)
							.append(
								function(){
									if(item.about.length>1){
										var link = "";
										if(inside)
											link = item.link_in;
										else
										 	link = item.link_out;
										
										return $("<label/>",{"html":"more info",css:{"margin-left":"3px","font-size":".7em"},"class":"hoverCard","data-image":item.image,"data-info":item.about,"data-link":link})	
									}
								}
								
							)
					}
				)
		)
		//alert(container.find("label.hoverCard").attr("data-info"))
		
	//container.find(".hoverCard")		
});
	//alert($(".hoverCard").attr("data-info"))
	$.each($(".hoverCard"),function(h,card){
		me=$(card)
		me.hovercard({"detailsHTML":me.attr("data-info")+"<br><a href='"+me.attr("data-link")+"'> even more info</a>","width":"400px","cardImgSrc":me.attr("data-image")});
	})
adultCategories.sort();
adultCategories = adultCategories.getUnique();

